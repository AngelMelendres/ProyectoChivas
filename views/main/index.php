              <p class="text-center">Existen tres versiones de sus origines en América Latina. La una dice que su historia se remonta a 1930, cuando el departamento de Antioquia importó buses desde el viejo continente para cubrir las rutas urbanas, sin embargo, el peso de los vehículos deterioraba las vías al punto de que, sus propietarios cambiaron la carrocería metálica por una estructura de madera. </p>
