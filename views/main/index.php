              <p class="text-center">Otra de las historias es que en 1923, los hermanos Antonio y Manuel Montoya compraron el primer camión para montar carga y pasajeros de Antioquia. Se trataba de un Stud Baker que había costado en ese entonces 500 pesos de contado. Un viejo artesano armó la carrocería y la pintó, bautizándola Ayacucho. El sonido del pito era comparado con el balido de una chiva, de ahí su nombre.

